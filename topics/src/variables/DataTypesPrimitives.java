package variables;

public class DataTypesPrimitives {
    public static void main(String[] args) {
        // byte example
        System.out.println("Primitives: ");
        byte byteVariable = 100;
        System.out.println("byteVariable: " + byteVariable);
        System.out.println("Description of byteVariable: Represents small integers, commonly used in file I/O operations and network communications.");
        System.out.println("-----------------------------------------------------------------------");

        // short example
        short shortVariable = 1000;
        System.out.println("shortVariable: " + shortVariable);
        System.out.println("Description of shortVariable: Represents integers in a wider range, often used in mathematical calculations and scientific applications.");
        System.out.println("-----------------------------------------------------------------------");

        // int example
        int intVariable = 100000;
        System.out.println("intVariable: " + intVariable);
        System.out.println("Description of intVariable: Represents whole numbers within a large range, commonly used for counting, indexing, and arithmetic operations.");
        System.out.println("-----------------------------------------------------------------------");

        // long example
        long longVariable = 1000000000L;
        System.out.println("longVariable: " + longVariable);
        System.out.println("Description of longVariable: Represents very large integer values, often used for storing timestamps, file sizes, or IDs.");
        System.out.println("-----------------------------------------------------------------------");

        // float example
        float floatVariable = 3.14f;
        System.out.println("floatVariable: " + floatVariable);
        System.out.println("Description of floatVariable: Represents decimal numbers with moderate precision, commonly used in graphics, simulations, and scientific calculations.");
        System.out.println("-----------------------------------------------------------------------");

        // double example
        double doubleVariable = 3.14159;
        System.out.println("doubleVariable: " + doubleVariable);
        System.out.println("Description of doubleVariable: Represents decimal numbers with high precision, widely used in engineering, finance, and data analysis.");
        System.out.println("-----------------------------------------------------------------------");

        // char example
        char charVariable = 'A';
        System.out.println("charVariable: " + charVariable);
        System.out.println("Description of charVariable: Represents a single character, commonly used in text processing, input/output operations, and user interfaces.");
        System.out.println("-----------------------------------------------------------------------");

        // boolean example
        boolean booleanVariable = true;
        System.out.println("booleanVariable: " + booleanVariable);
        System.out.println("Description of booleanVariable: Represents a true or false value, used for logical conditions, decision-making, and control flow in programs.");
        System.out.println("-----------------------------------------------------------------------");
    }
}

